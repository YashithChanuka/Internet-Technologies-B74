// Dynamic Typing - JavaScript is a dynamically typed language, which means that you don't have to specify the data type of a variable when declaring it. The data type of a variable can change during the execution of a program and JavaScript will automatically convert the variable to the correct data type.

let x = 10;
console.log(typeof x);

x = "Hello";
console.log(typeof x);

x = true;
console.log(typeof x);

